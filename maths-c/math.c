int additon(int a , int b)
{
	int c=a+b;
	return c;
}

int substraction(int a ,int b)
{
	int c=a-b;
	return c;
}

long int multiplication(int a, int b)
{
	int c=(long int)a*b;
	return c;
}
	
float divisible(int a,int b)
{
	float c=(float)a/b;
	return c;
}
